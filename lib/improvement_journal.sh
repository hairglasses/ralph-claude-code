#!/bin/bash
# Improvement Journal for Ralph Loop
# Records per-loop retrospectives and triggers periodic meta-improvement cycles

# Source date utilities for timestamps
source "$(dirname "${BASH_SOURCE[0]}")/date_utils.sh"

# State files
RALPH_DIR="${RALPH_DIR:-.ralph}"
IMPROVEMENT_JOURNAL_FILE="$RALPH_DIR/improvement_journal.jsonl"
META_IMPROVEMENTS_FILE="$RALPH_DIR/meta_improvements.md"
SUCCESSFUL_LOOP_COUNT_FILE="$RALPH_DIR/.successful_loop_count"

# Initialize improvement journal state
init_improvement_journal() {
    mkdir -p "$RALPH_DIR"

    if [[ ! -f "$IMPROVEMENT_JOURNAL_FILE" ]]; then
        touch "$IMPROVEMENT_JOURNAL_FILE"
    fi

    if [[ ! -f "$META_IMPROVEMENTS_FILE" ]]; then
        cat > "$META_IMPROVEMENTS_FILE" << 'EOF'
# Ralph Meta-Improvement Log

Auto-generated by the improvement journal system.
Each entry is produced by a sonnet meta-improvement cycle after every N successful loops.

---

EOF
    fi

    if [[ ! -f "$SUCCESSFUL_LOOP_COUNT_FILE" ]]; then
        echo "0" > "$SUCCESSFUL_LOOP_COUNT_FILE"
    fi
}

# Extract retrospective from Claude's output
# Looks for ## Loop Retrospective section with Worked/Didn't/Suggest lines
# Usage: extract_retrospective output_file
# Prints JSON object with worked/didnt/suggest fields
extract_retrospective() {
    local output_file=$1

    if [[ ! -f "$output_file" ]]; then
        echo '{"worked":"","didnt":"","suggest":""}'
        return
    fi

    # Try to extract from the output — look for the three required lines
    # Handle both raw text and JSON-wrapped output
    local content
    content=$(cat "$output_file")

    # Extract text content from JSON if needed
    if echo "$content" | jq -e '.result // .content' &>/dev/null; then
        content=$(echo "$content" | jq -r '
            if .result then
                (.result | if type == "array" then
                    [.[] | select(.type == "text") | .text] | join("\n")
                elif type == "string" then . else "" end)
            elif .content then
                (.content | if type == "array" then
                    [.[] | select(.type == "text") | .text] | join("\n")
                elif type == "string" then . else "" end)
            else ""
            end' 2>/dev/null || cat "$output_file")
    fi

    local worked didnt suggest
    worked=$(echo "$content" | grep -i '^\*\*Worked\*\*:' | head -1 | sed 's/^\*\*Worked\*\*:[[:space:]]*//' || true)
    didnt=$(echo "$content" | grep -i '^\*\*Didn.t\*\*:' | head -1 | sed "s/^\*\*Didn.t\*\*:[[:space:]]*//" || true)
    suggest=$(echo "$content" | grep -i '^\*\*Suggest\*\*:' | head -1 | sed 's/^\*\*Suggest\*\*:[[:space:]]*//' || true)

    # Also try without bold markers
    if [[ -z "$worked" ]]; then
        worked=$(echo "$content" | grep -i '^- Worked:' | head -1 | sed 's/^- Worked:[[:space:]]*//' || true)
    fi
    if [[ -z "$didnt" ]]; then
        didnt=$(echo "$content" | grep -i '^- Didn.t:' | head -1 | sed "s/^- Didn.t:[[:space:]]*//" || true)
    fi
    if [[ -z "$suggest" ]]; then
        suggest=$(echo "$content" | grep -i '^- Suggest:' | head -1 | sed 's/^- Suggest:[[:space:]]*//' || true)
    fi

    jq -n \
        --arg worked "$worked" \
        --arg didnt "$didnt" \
        --arg suggest "$suggest" \
        '{worked: $worked, didnt: $didnt, suggest: $suggest}'
}

# Record an improvement note after a successful loop
# Usage: record_improvement_note loop_count phase model tasks_done duration_s cost_usd productive files_changed output_file
record_improvement_note() {
    local loop_count=$1
    local phase=$2
    local model=$3
    local tasks_done=$4
    local duration_s=$5
    local cost_usd=$6
    local productive=$7
    local files_changed=$8
    local output_file=$9

    init_improvement_journal

    # Extract retrospective from Claude's output
    local retro
    retro=$(extract_retrospective "$output_file")

    local worked didnt suggest
    worked=$(echo "$retro" | jq -r '.worked // ""')
    didnt=$(echo "$retro" | jq -r '.didnt // ""')
    suggest=$(echo "$retro" | jq -r '.suggest // ""')

    local ts
    ts=$(get_iso_timestamp)

    # Build and append journal entry
    local entry
    entry=$(jq -n \
        --arg ts "$ts" \
        --argjson loop "$loop_count" \
        --arg phase "$phase" \
        --arg model "$model" \
        --argjson tasks_done "$tasks_done" \
        --argjson duration_s "$duration_s" \
        --argjson cost_usd "$cost_usd" \
        --argjson productive "$productive" \
        --argjson files_changed "$files_changed" \
        --arg worked "$worked" \
        --arg didnt "$didnt" \
        --arg suggest "$suggest" \
        '{ts: $ts, loop: $loop, phase: $phase, model: $model, tasks_done: $tasks_done, duration_s: $duration_s, cost_usd: $cost_usd, productive: $productive, files_changed: $files_changed, worked: $worked, didnt: $didnt, suggest: $suggest}')

    echo "$entry" >> "$IMPROVEMENT_JOURNAL_FILE"
}

# Increment successful loop counter
increment_successful_loops() {
    init_improvement_journal
    local count
    count=$(cat "$SUCCESSFUL_LOOP_COUNT_FILE" 2>/dev/null || echo "0")
    count=$((count + 1))
    echo "$count" > "$SUCCESSFUL_LOOP_COUNT_FILE"
    echo "$count"
}

# Get successful loop count
get_successful_loop_count() {
    if [[ -f "$SUCCESSFUL_LOOP_COUNT_FILE" ]]; then
        cat "$SUCCESSFUL_LOOP_COUNT_FILE"
    else
        echo "0"
    fi
}

# Check if meta-improvement should run
# Returns 0 if yes, 1 if no
should_run_meta_improvement() {
    local interval=${RALPH_META_IMPROVEMENT_INTERVAL:-10}
    local count
    count=$(get_successful_loop_count)
    count=$((count + 0))

    if [[ $count -eq 0 ]]; then
        return 1
    fi

    if [[ $((count % interval)) -eq 0 ]]; then
        return 0
    fi

    return 1
}

# Run meta-improvement cycle
# Reads last N journal entries, has sonnet analyze patterns, outputs recommendations
run_meta_improvement() {
    local claude_cmd=${CLAUDE_CODE_CMD:-claude}

    init_improvement_journal

    local interval=${RALPH_META_IMPROVEMENT_INTERVAL:-10}
    local count
    count=$(get_successful_loop_count)

    echo "Running meta-improvement cycle (after $count successful loops)..." >&2

    # Get last N journal entries
    local entries
    entries=$(tail -"$interval" "$IMPROVEMENT_JOURNAL_FILE")

    if [[ -z "$entries" ]]; then
        echo "No journal entries to analyze" >&2
        return 0
    fi

    # Build the meta-improvement prompt
    local prompt
    prompt="You are analyzing Ralph loop improvement journal entries to identify patterns and suggest tuning.

Here are the last $interval journal entries (JSONL):

$entries

Analyze these entries and provide:

1. **Patterns**: What patterns do you see in what worked vs didn't?
2. **Cost Efficiency**: Are there opportunities to reduce cost (e.g., more sonnet usage)?
3. **Productivity**: What correlates with high task completion?
4. **Tuning Recommendations**: Suggest specific RALPHRC_SET values for numeric parameters only.

For tuning, output lines like:
RALPHRC_SET: CLAUDE_TIMEOUT_MINUTES=20
RALPHRC_SET: CB_NO_PROGRESS_THRESHOLD=5

Only suggest changes to numeric tuning parameters. Do not suggest changes to paths, commands, or tools.

Also list any manual-review items that need human attention."

    # Run sonnet with 5-minute timeout for meta-improvement
    local meta_output
    local meta_exit_code=0
    meta_output=$(timeout 300 "$claude_cmd" --model sonnet -p "$prompt" --output-format text 2>&1) || meta_exit_code=$?

    if [[ $meta_exit_code -ne 0 ]]; then
        echo "Meta-improvement call failed (exit $meta_exit_code)" >&2
        return 0  # Non-fatal
    fi

    # Append results to meta_improvements.md
    local ts
    ts=$(get_iso_timestamp)
    {
        echo ""
        echo "## Meta-Improvement Cycle — $ts (after loop $count)"
        echo ""
        echo "$meta_output"
        echo ""
        echo "---"
    } >> "$META_IMPROVEMENTS_FILE"

    # Extract and auto-apply safe RALPHRC_SET values
    local ralphrc_sets
    ralphrc_sets=$(echo "$meta_output" | grep -E '^RALPHRC_SET:' || true)

    if [[ -n "$ralphrc_sets" ]]; then
        echo "Auto-applying tuning recommendations:" >&2
        while IFS= read -r line; do
            local key_value
            key_value=$(echo "$line" | sed 's/^RALPHRC_SET:[[:space:]]*//')
            local key val
            key=$(echo "$key_value" | cut -d= -f1)
            val=$(echo "$key_value" | cut -d= -f2-)

            # Safety: only apply numeric values
            if [[ "$val" =~ ^[0-9]+$ ]]; then
                echo "  Setting $key=$val" >&2
                # Update in-memory (current session)
                export "$key=$val"
            else
                echo "  Skipping $key=$val (non-numeric)" >&2
            fi
        done <<< "$ralphrc_sets"
    fi

    echo "Meta-improvement cycle complete. Results saved to $META_IMPROVEMENTS_FILE" >&2
}

# Export functions
export -f init_improvement_journal
export -f extract_retrospective
export -f record_improvement_note
export -f increment_successful_loops
export -f get_successful_loop_count
export -f should_run_meta_improvement
export -f run_meta_improvement
